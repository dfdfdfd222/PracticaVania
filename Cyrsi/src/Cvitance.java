
public class Cvitance {
    
    private String Opisan;
    private int sum;
    private String Curs;

    public Cvitance(String Opisan, int sum, String Curs) {
        
        this.Opisan=Opisan;
        this.sum= sum;
        this.Curs=Curs;
        
    }
    
    public Cvitance() {
        
    }
    public String getOpisan() {
            return Opisan;       
        }
        public void setOpisan(String Opisan){
            this.Opisan = Opisan;
        }
        
    public int getsum() {
            return sum;       
        }
        public void setsum(int sum){
            this.sum = sum;
        }
    public String getCurs() {
            return Curs;       
        }
        public void setCurs(String Curs){
            this.Curs = Curs;
        }
    
    
    
    
    
    
    
    
    
    
    
}
